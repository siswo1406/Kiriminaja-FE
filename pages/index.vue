    selectionCssClass: "no-shadow p-0 bg-transparent fw-medium border-0",
    dropdownCssClass: "border-0 main-radius",
    cacheDataSource: [],
    minimumInputLength: 3,
    theme: 'bootstrap4',
    placeholder: "Kecamatan Asal",
    ajax: {
      url: "https://kiriminaja.com/public-address",
      type: "get",
      dataType: 'json',
      quietMillis: 50,
      data: function (params) {
        return {
          search: params.term,
          // coverage: true,
          decrypt: false
        };
      },
      processResults: function (response) {
        return {
            results: response
        };
      },
      cache: true
    }
  }).on('keyup change', function () {
    localStorage['set_origin'] = JSON.stringify({
      id: $('.set-origin option:selected').val(),
      text: $('.set-origin option:selected').html()
    });
  });
